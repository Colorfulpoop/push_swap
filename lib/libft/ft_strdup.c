#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dst;
	int		i;

	i = 0;
	dst = malloc(ft_strlen(src) * sizeof(char) + 1);
	if (dst == NULL)
		return (NULL);
	while (src[i] != 0)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
